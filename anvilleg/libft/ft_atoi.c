int	ft_atoi(const char *str)
{
	int nbr;
	int signo;
	int i;

	signo = 1;
	nbr = 0;
	i = 0;
	while ((*str == '\t') || (*str == '\n') || (*str == '\v') || (*str == '\f')
			|| (*str == '\r') || (*str == 32))
	{
		str++;
	}
	if ((*str == '-') || (*str == '+'))
	{
		if (*str == '-')
			signo = signo * (-1);
		str++;
	}
	while ((*str >= '0') && (*str <= '9'))
	{
		nbr = (nbr * 10) + (*str - '0');
		str++;
	}
	return (nbr * signo);
}
